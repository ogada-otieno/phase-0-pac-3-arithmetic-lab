function add(a, b) {
    let sum = a + b;
    return sum;
}

function subtract(a, b) {
    let sub = a - b;
    return sub;
}

function multiply(a, b) {
    let product = a * b;
    return product; 
}

function divide(a, b) {
    let division = a / b;
    return division;
}

function increment(n) {
    n+= 1;
    return n;
}

function decrement(n) {
    n -= 1;
    return n;
}

function makeInt(n) {
    return parseFloat(n);
}

function preserveDecimal(n) {
    return parseFloat(n);
}